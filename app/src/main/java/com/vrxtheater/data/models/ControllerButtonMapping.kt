package com.vrxtheater.data.models

/**
 * Enum representing controller button mappings
 */
enum class ControllerButtonMapping {
    // Face buttons
    BUTTON_A,
    BUTTON_B,
    BUTTON_X,
    BUTTON_Y,
    
    // D-pad
    DPAD_UP,
    DPAD_DOWN,
    DPAD_LEFT,
    DPAD_RIGHT,
    
    // Shoulder buttons
    BUTTON_L1,
    BUTTON_R1,
    BUTTON_L2,
    BUTTON_R2,
    
    // Analog stick buttons
    BUTTON_L3,
    BUTTON_R3,
    
    // Special buttons
    BUTTON_START,
    BUTTON_SELECT,
    
    // System buttons
    BUTTON_HOME,
    BUTTON_BACK
}